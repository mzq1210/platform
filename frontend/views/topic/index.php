    // 注册'infinite'事件处理函数
    $(document).on('infinite', '.infinite-scroll', function() {

        // 如果正在加载，则退出
        if (loading) return;

        // 设置flag
        loading = true;
        var tabIndex = 0;
        if($(this).find('.infinite-scroll.active').attr('id') == "tab1"){
            tabIndex = 0;
        }
        if($(this).find('.infinite-scroll.active').attr('id') == "tab2"){
            tabIndex = 1;
        }
        if($(this).find('.infinite-scroll.active').attr('id') == "tab3"){
            tabIndex = 2;
        }
        if($(this).find('.infinite-scroll.active').attr('id') == "tab4"){
            tabIndex = 3;
        }
        lastIndex = $('.list-container').eq(tabIndex).find('li').length;

        // 模拟1s的加载过程
        setTimeout(function() {
            // 重置加载flag
            loading = false;

            if (lastIndex >= maxItems) {
                // 加载完毕，则注销无限加载事件，以防不必要的加载
                $('.infinite-scroll-preloader').eq(tabIndex).hide();
                return;
            }

            // 添加新条目
            if (tabIndex == 0) {
                addItems(itemsPerLoad, lastIndex);
            }
            if (tabIndex == 1) {
                addItems1(itemsPerLoad, lastIndex);
            }
            if (tabIndex == 2) {
                addItems2(itemsPerLoad, lastIndex);
            }
            if (tabIndex == 3) {
                addItems3(itemsPerLoad, lastIndex);
            }

            // 更新最后加载的序号
            lastIndex =  $('.list-container').eq(tabIndex).find('li').length;
            $.refreshScroller();
        }, 1000);
    });


